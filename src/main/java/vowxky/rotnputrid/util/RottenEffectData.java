public class RottenEffectData {
    private final int duration;
    private final int range;

    public RottenEffectData(int duration, int range) {
        this.duration = duration;
        this.range = range;
    }

    public int getDuration() {
        return duration;
    }

    public int getRange() {
        return range;
    }
}
